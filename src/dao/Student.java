package dao;

public class Student {
	private int Student_ID;
	private String Student_Name;
	private String Student_Sex;
	private int Student_Age;
	private Long Student_Telephone;
	private String Student_Class;
	private String Student_Birthday;
	private String Student_Nativeplace;
	private String Student_Nation;
	private String Student_Major;
	public int getStudent_ID() {
		return Student_ID;
	}
	public void setStudent_ID(int student_ID) {
		Student_ID = student_ID;
	}
	public String getStudent_Name() {
		return Student_Name;
	}
	public void setStudent_Name(String student_Name) {
		Student_Name = student_Name;
	}
	public String getStudent_Sex() {
		return Student_Sex;
	}
	public void setStudent_Sex(String student_Sex) {
		Student_Sex = student_Sex;
	}
	public int getStudent_Age() {
		return Student_Age;
	}
	public void setStudent_Age(int student_Age) {
		Student_Age = student_Age;
	}

	public String getStudent_Class() {
		return Student_Class;
	}
	public void setStudent_Class(String student_Class) {
		Student_Class = student_Class;
	}
	public String getStudent_Birthday() {
		return Student_Birthday;
	}
	public void setStudent_Birthday(String student_Birthday) {
		Student_Birthday = student_Birthday;
	}
	public String getStudent_Nativeplace() {
		return Student_Nativeplace;
	}
	public void setStudent_Nativeplace(String student_Nativeplace) {
		Student_Nativeplace = student_Nativeplace;
	}
	public String getStudent_Nation() {
		return Student_Nation;
	}
	public void setStudent_Nation(String student_Nation) {
		Student_Nation = student_Nation;
	}
	public String getStudent_Major() {
		return Student_Major;
	}
	public void setStudent_Major(String student_Major) {
		Student_Major = student_Major;
	}

	public Long getStudent_Telephone() {
		return Student_Telephone;
	}

	public void setStudent_Telephone(Long student_Telephone) {
		Student_Telephone = student_Telephone;
	}
}
